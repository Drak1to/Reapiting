package CarCorrect;

public class Wheel2 {
    private int number;

    public Wheel2(int number) {
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    @Override
    public String toString() {
        return "Wheel2{" +
                "number=" + number +
                '}';
    }
}
